package Arrays;

public class NumbersSumInArray {
    public static void main(String[] args) {
        int[] arr;
        arr = new int[5];
        arr[0] = 100;
        arr[1] = 256;
        arr[2] = 367;
        arr[3] = 40;
        arr[4] = 599;

        for (int i = 0; i < arr.length; i++)
            System.out.println("Element at index " + i + " : " + arr[i]);
    }
}
